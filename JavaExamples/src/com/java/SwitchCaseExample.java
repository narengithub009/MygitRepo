package com.java;

public class SwitchCaseExample {
	public static void main(String[] args) {
		int i=7;
		switch (i) {
		case 10:
			System.out.println("i value is "+i);
			break;
		case 1:
			System.out.println("i value is "+i);
			break;	
		default:
			System.out.println("i is nagetaive");
			break;
		}
	}
}
